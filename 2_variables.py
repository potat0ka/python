### **Variables in Python**
# A **variable** is a container used to store data in Python. It can hold different types of values, such as numbers, text (strings), and Boolean values.  

#âœ… **Example of Variable Declaration:**
price = 10  # Assigning the value 10 to the variable 'price'
price = 20  # Updating 'price' to 20
name = 'potato'  # Assigning a string value
is_published = True  # Boolean value (True or False)

print(price)  # Output: 20

#ðŸ’¡ **Key Points About Variables:**  
#1. **Variables store values** that can be changed later in the program.  
#2. Variable names **should be descriptive** (e.g., `price`, `name`, `is_published`).  
#3. Strings (`'text'` or `"text"`) must be enclosed in **quotes**.  
#4. Boolean values (`True` or `False`) are written without quotes.  


